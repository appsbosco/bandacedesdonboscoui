function buildDefaultSlug(article) {
  return article.slug ?? String(article.id);
}

function localizeContent(article, lang) {
  const translations = articleTranslations[article.id]?.[lang];

  return {
    ...article,
    slug: translations?.slug ?? buildDefaultSlug(article),
    title: translations?.title ?? article.title,
    description: translations?.description ?? article.description,
    type:
      translations?.type ??
      (lang === "en" ? typeTranslations[article.type] ?? article.type : article.type),
    date: translations?.date ?? article.date,
    content: translations?.content ?? article.content,
    lecture:
      translations?.lecture ??
      (lang === "en" ? lectureTranslations[article.lecture] ?? article.lecture : article.lecture),
  };
}

export function getBlogArticles(lang = "es") {
  return baseArticles.map((article) => localizeContent(article, lang));
}

export function getBlogArticleBySlug(lang = "es", slug = "") {
  const articles = getBlogArticles(lang);
  return articles.find((article) => article.slug === slug || String(article.id) === slug);
}

export function getBlogArticleHref(lang, article) {
  const localizedArticle = localizeContent(article, lang);
  return `/${lang}/blog/${localizedArticle.slug}`;
}
